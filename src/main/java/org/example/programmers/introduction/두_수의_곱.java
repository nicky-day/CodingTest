package org.example.programmers.introduction;

class 두_수의_곱 {
    public int solution(int num1, int num2) {
        return num1 * num2;
    }
}

class Main1 {
    public static void main(String[] args) {
        두_수의_곱 problem = new 두_수의_곱();
        System.out.println(problem.solution(3, 4));
    }
}
