class 두_수의_차 {
    public int solution(int num1, int num2) {
        return num1 - num2;
    }
}

class Main2 {
    public static void main(String[] args) {
        두_수의_차 problem = new 두_수의_차();
        System.out.println(problem.solution(2, 3));
    }
}
